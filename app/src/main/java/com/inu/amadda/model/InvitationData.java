package com.inu.amadda.model;

public class InvitationData {
    public int share;
    public String group_name, inviter_name, inviter_id, memo;

    public int getShare() {
        return share;
    }

    public String getGroup_name() {
        return group_name;
    }

    public String getInviter_name() {
        return inviter_name;
    }

    public String getInviter_id() {
        return inviter_id;
    }

    public String getMemo() {
        return memo;
    }
}
